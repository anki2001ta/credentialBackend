const express = require("express");
const validate = require("../middlewares/validate");
const authValidation = require("../validations/auth.validation");
const authController = require("../controllers/auth.controller");
const auth = require("../middlewares/auth");

const router = express.Router();

router.post(
  "/organization/signup",
  validate(authValidation.orgSignup),
  authController.organizationRegistration
);
router.post(
  "/organization/login",
  validate(authValidation.login),
  authController.orglogin
);
router.post(
  "/organization/forgot-password",
  validate(authValidation.forgotPassword),
  authController.orgforgotPassword
);
router.post(
  "/organization/refresh-tokens",
  validate(authValidation.refreshTokens),
  authController.refreshOrgTokens
);
router.post(
  "/organization/reset-password",
  validate(authValidation.resetPassword),
  authController.resetOrganizationPassword
);
router.post(
  "/organization/verify-email",
  validate(authValidation.verifyEmail),
  authController.orgverifyEmail
);
router.put(
  "/organization/profile-settings/:id",
  validate(authValidation.refreshTokens),
  authController.updateOrganizationProfileSettings
);

router.post(
  "/individual/signup",
  validate(authValidation.individualSignup),
  authController.individualRegisteration
);
router.post(
  "/individual/login",
  validate(authValidation.login),
  authController.individualLogin
);
router.post(
  "/individual/refresh-tokens",
  validate(authValidation.refreshTokens),
  authController.refreshIndividualTokens
);
router.post(
  "/individual/forgot-password",
  validate(authValidation.forgotPassword),
  authController.individualforgotPassword
);
router.post(
  "/individual/reset-password",
  validate(authValidation.resetPassword),
  authController.resetIndividualPass
);
router.post(
  "/individual/verify-email",
  validate(authValidation.verifyEmail),
  authController.individualverifyEmail
);
router.put(
  "/individual/profile-settings/:id",
  // validate(authValidation.refreshTokens),
  authController.updateIndividualProfileSettings
);

router.post("/logout", validate(authValidation.logout), 
authController.logout);


router.post(
  "/send-verification-email",
  auth(),
  authController.sendVerificationEmail
);

module.exports = router;

