router.get("/organization/group", getAllGroupsCollection);
router.delete("/organization/group", delGroupCollectionData);
router.put("/organization/group/:id", updateGroupCollectionData);
router.post("/organization/group/:id", addCoordinates);

router.post("/individual/addCertificate", individualAddCredential);
router.get("/individual/getIndividualAddedCredentials/:userId",getIndividualAddedCredentials);

