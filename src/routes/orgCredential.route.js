const express = require("express");
const router = express.Router();
const validate = require("../middlewares/validate");
const authValidation = require("../validations/auth.validation");
const authController = require("../controllers/auth.controller");
const auth = require("../middlewares/auth");

const {
  createGroup,
  delGroupCollectionData,
  getAllGroupsCollection,
  updateGroupCollectionData,
  addCoordinates,
  retrieveMetaData,
  getTemplateData,
  postIssuedCredentials,
  individualAddCredential,
  getIndividualAddedCredentials,
  getIssuedCredentials,
} = require("../controllers/group.controller");

router.post("/organization/group/metaData", retrieveMetaData);
router.get("/organization/group/templates/:groupId", getTemplateData);
router.post("/organization/group/issuedCredentials", postIssuedCredentials);
router.get(
  "/organization/group/issuedCredentials/:userEmail",
  getIssuedCredentials
);

